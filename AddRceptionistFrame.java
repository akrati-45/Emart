    private boolean validateInput() {
        return !(txtEmpName.getText().isEmpty() || txtUserId.getText().isEmpty() || txtPassword.getText().isEmpty() || txtRePassword.getText().isEmpty());
    }
     private void showId() {
       try
       {
           ArrayList<String> empId=new ArrayList<>();
           empId=(ArrayList<String>)EmployeeDAO.allReceptionist();
           for(String e:empId)
           {
               jcEmpId.addItem(e);
           }
       }
       catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Problem In The Database!!", "Error!!", JOptionPane.ERROR_MESSAGE);
            System.out.println("SQLException is "+ex);
            ex.printStackTrace();
        }
    }
      private void clearText() {
        txtEmpName.setText("");
        txtPassword.setText("");
        jcEmpId.setSelectedIndex(0);
        txtUserId.setText("");
        txtRePassword.setText("");
    }
