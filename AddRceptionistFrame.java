    private void btnAddReceptionistActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddReceptionistActionPerformed

        if(!validateInput())
        {
            JOptionPane.showMessageDialog(null,"Please fill all the fields!", "Error Message!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try
        {
            UserPojo u=new UserPojo();
            u.setEmpId(jcEmpId.getSelectedItem().toString());
            u.setUserId(txtUserId.getText());
            u.setUserName(txtEmpName.getText());
            char [] password=txtPassword.getPassword();
            String pwd=String.valueOf(password);
            while(!(pwd.equals(txtRePassword.getText().trim())))
            {
                JOptionPane.showMessageDialog(null,"Password not matches!", "Error Message!",JOptionPane.ERROR_MESSAGE);
                txtRePassword.setText("");
                txtRePassword.requestFocus();
            }
            u.setPassword(pwd);
            u.setUserType("Receptionist");
            boolean b=UserDAO.addReceptionist(u);
           
            if(!b)
            {
                JOptionPane.showMessageDialog(null,"Record not inserted!", "Error Message!",JOptionPane.ERROR_MESSAGE);
                return;
            }
            JOptionPane.showMessageDialog(null,"Record inserted successfully!", "success!",JOptionPane.INFORMATION_MESSAGE);
            clearText();
        }
        catch(SQLException ex)
        {
             System.out.println("add is error");
            JOptionPane.showMessageDialog(null,"Problem In The Database!!", "Error!!", JOptionPane.ERROR_MESSAGE);
            System.out.println("SQLException is "+ex);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnAddReceptionistActionPerformed

