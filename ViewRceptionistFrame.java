    private void showData() {
        try
        {
            List<UserPojo> empList=UserDAO.getAllReceptionist();
            DefaultTableModel tm=(DefaultTableModel)jtAllRecep.getModel();
            
            Object [] rows =new Object[4];
            for(UserPojo u:empList)
            {
                rows[0]=u.getEmpId();
                rows[1]=u.getUserName();
                rows[2]=u.getUserId();
                rows[3]=u.getPassword();
                tm.addRow(rows);
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Problem In The Database!!", "Error!!", JOptionPane.ERROR_MESSAGE);
            System.out.println("SQLException is "+ex);
        }
    }

