    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(!validateInputs())
        {
            JOptionPane.showMessageDialog(null,"Please fill all the fields!", "Incomplete!!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try
        {
            ProductsPojo p=new ProductsPojo();
            p.setProductId(txtProductId.getText().trim());
            p.setProductName(txtProductName.getText().trim());
            p.setProductCompany(txtprdtcmpy.getText().trim());
            p.setProductPrice(Double.parseDouble(txtPrice.getText().trim()));
            p.setOurPrice(Double.parseDouble(txtOurPrice.getText().trim()));
            int n =jcTax.getSelectedItem().toString().trim().length();
            String tax=jcTax.getSelectedItem().toString().trim().substring(0,n-1);
            p.setTax(Integer.parseInt(tax));
            p.setQuantity(Integer.parseInt(txtquantity.getText().trim()));
            boolean result=ProductsDAO.UpdateProducts(p);
            if(result)
            {
                JOptionPane.showMessageDialog(null,"Record Updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                clearText();
                return;
            }
            JOptionPane.showMessageDialog(null,"Record not Updated!", "Error!!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Problem In The Database!!", "Error!!", JOptionPane.ERROR_MESSAGE);
            System.out.println("SQLException is "+ex);
            ex.printStackTrace();
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null,"Please enter Digits only!!", "Error!!", JOptionPane.ERROR_MESSAGE);
            System.out.println("SQLException is "+ex);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void jtProductDetailsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtProductDetailsMouseClicked
        int row=jtProductDetails.getSelectedRow();
        DefaultTableModel tm=(DefaultTableModel)jtProductDetails.getModel();
        txtProductId.setText(tm.getValueAt(row, 0).toString());
        txtProductName.setText(tm.getValueAt(row, 1).toString());
        txtprdtcmpy.setText(tm.getValueAt(row, 2).toString());
        txtPrice.setText(tm.getValueAt(row, 3).toString());
        txtOurPrice.setText(tm.getValueAt(row, 4).toString());
        txtquantity.setText(tm.getValueAt(row, 5).toString());
        jcTax.setSelectedItem(tm.getValueAt(row, 6));
    }//GEN-LAST:event_jtProductDetailsMouseClicked

