    private void getDetails() {
         try
        {
            List<ProductsPojo> p_List=ProductsDAO.getAllProducts();
            if(p_List==null)
            {
                JOptionPane.showMessageDialog(null,"No Products are Present", "Error!!", JOptionPane.ERROR_MESSAGE);
                return;
            }
            DefaultTableModel tm=(DefaultTableModel)jtProductDetails.getModel();
            Object [] rows =new Object[7];
            for(ProductsPojo p:p_List)
            {
                rows[0]=p.getProductId();
                rows[1]=p.getProductName();
                rows[2]=p.getProductCompany();
                rows[3]=p.getProductPrice();
                rows[4]=p.getOurPrice();
                rows[5]=p.getQuantity();
                rows[6]=p.getTax()+"%";
                tm.addRow(rows);
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Problem In The Database!!", "Error!!", JOptionPane.ERROR_MESSAGE);
            System.out.println("SQLException is "+ex);
        }
    }
